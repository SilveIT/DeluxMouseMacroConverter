namespace MouseMacroConverter.Delux
{
    enum DeluxAction : byte
    {
        LeftButton = 0xF0,
        RightButton = 0xF1,
        MiddleButton = 0xF2,
        MoveLeft = 0xF9,
        MoveRight = 0xFA,
        MoveUp = 0xF7,
        MoveDown = 0xF8,
        ScrollUp = 0xF5,
        ScrollDown = 0xF6
    }
}
